static int slotNullEvent(PARAM *p, DATA *d)
{
  if(p == NULL || d == NULL) return -1;
  const char *s=mbx_out.read(0);
  int i = 0;
  while(s[0]) {
      if(s[0] != '\n'){
          if(!i) d->arq = fopen("file.html", "wt");
          fprintf(d->arq, "%s<p>%s</p>", ++i ? "" : "<!DOCTYPE html><html><body>" , s);
      }
      s=mbx_out.read(0);
  }
  if(i){
      fprintf(d->arq, "</body></body>"); 
      fclose(d->arq);
      pvDownloadFile(p,"file.html");
      pvSetSource(p, _RESPOSTA_, "file.html");
  }
  return 0;
}

